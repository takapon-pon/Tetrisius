// SRS壁キックオフセット（I以外）
const WALL_KICK_OFFSETS_NORMAL: Record<string, Position[]> = {
  '0>1': [{ x: -1, y: 0 }, { x: -1, y: -1 }, { x: 0, y: 2 }, { x: -1, y: 2 }],
  '1>2': [{ x: 1, y: 0 }, { x: 1, y: 1 }, { x: 0, y: -2 }, { x: 1, y: -2 }],
  '2>3': [{ x: 1, y: 0 }, { x: 1, y: -1 }, { x: 0, y: 2 }, { x: 1, y: 2 }],
  '3>0': [{ x: -1, y: 0 }, { x: -1, y: 1 }, { x: 0, y: -2 }, { x: -1, y: -2 }],
};

// SRS壁キックオフセット（Iミノ）
const WALL_KICK_OFFSETS_I: Record<string, Position[]> = {
  '0>1': [{ x: -2, y: 0 }, { x: 1, y: 0 }, { x: -2, y: 1 }, { x: 1, y: -2 }],
  '1>2': [{ x: -1, y: 0 }, { x: 2, y: 0 }, { x: -1, y: -2 }, { x: 2, y: 1 }],
  '2>3': [{ x: 2, y: 0 }, { x: -1, y: 0 }, { x: 2, y: -1 }, { x: -1, y: 2 }],
  '3>0': [{ x: 1, y: 0 }, { x: -2, y: 0 }, { x: 1, y: 2 }, { x: -2, y: -1 }],
};

export function getWallKickOffsets(shape: TetrominoShape, fromRotation: number, toRotation: number): Position[] {
  const key = `${fromRotation}>${toRotation}`;
  if (shape === 'I') {
    return WALL_KICK_OFFSETS_I[key] || [];
  }
  return WALL_KICK_OFFSETS_NORMAL[key] || [];
}

export function getBlockCells(shape: TetrominoShape, rotation: number, position: Position): Position[] {
  const localCells = TETROMINOES[shape][rotation];
  return localCells.map(cell => ({
    x: cell.x + position.x,
    y: cell.y + position.y,
  }));
}

const ALL_SHAPES: TetrominoShape[] = ['I', 'O', 'T', 'S', 'Z', 'J', 'L'];

export function getRandomShape(): TetrominoShape {
  return ALL_SHAPES[Math.floor(Math.random() * ALL_SHAPES.length)];
}
